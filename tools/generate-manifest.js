const fs = require("fs");
const path = require("path");
const { execFileSync } = require("child_process");

const ROOT = path.resolve(__dirname, "..");
const ASSET_ROOT = path.join(ROOT, "character_img");
const CSV_PATH = path.join(ASSET_ROOT, "metadata.csv");
const OUT_PATH = path.join(ROOT, "characters.js");
const FEATURE_SIZE = 16;
const REFERENCE_ROI_VARIANTS = [
  {
    name: "normal",
    portrait: { x: 0.12, y: 0.02, w: 0.76, h: 0.70 },
    upper: { x: 0.16, y: 0.01, w: 0.68, h: 0.46 },
    face: { x: 0.18, y: 0.00, w: 0.64, h: 0.32 }
  },
  {
    name: "high",
    portrait: { x: 0.12, y: 0.00, w: 0.76, h: 0.62 },
    upper: { x: 0.16, y: 0.00, w: 0.68, h: 0.40 },
    face: { x: 0.18, y: 0.00, w: 0.64, h: 0.28 }
  },
  {
    name: "low",
    portrait: { x: 0.12, y: 0.08, w: 0.76, h: 0.70 },
    upper: { x: 0.16, y: 0.08, w: 0.68, h: 0.46 },
    face: { x: 0.18, y: 0.09, w: 0.64, h: 0.32 }
  },
  {
    name: "tight",
    portrait: { x: 0.22, y: 0.02, w: 0.56, h: 0.62 },
    upper: { x: 0.24, y: 0.01, w: 0.52, h: 0.42 },
    face: { x: 0.25, y: 0.00, w: 0.50, h: 0.30 }
  }
];

function main() {
  const rows = parseCsv(fs.readFileSync(CSV_PATH, "utf8"));
  const files = indexAssetFiles(ASSET_ROOT);
  const entries = [];

  rows.forEach((row, index) => {
    const directPath = path.join(ASSET_ROOT, row.character, row.filename);
    const actualPath = fs.existsSync(directPath) ? directPath : files.get(row.filename.toLowerCase());
    if (!actualPath) {
      console.warn(`[skip] missing ${row.character}/${row.filename}`);
      return;
    }

    const image = decodeImage(actualPath);
    const desc = describeReference(image);
    const relPath = toWebPath(path.relative(ROOT, actualPath));
    entries.push({
      character: row.character,
      skin: row.skin || "default",
      filename: row.filename,
      path: `./${relPath}`,
      desc
    });

    if ((index + 1) % 25 === 0) {
      console.log(`processed ${index + 1}/${rows.length}`);
    }
  });

  const body = [
    "/* Generated by tools/generate-manifest.js. Do not edit by hand. */",
    "window.CHARACTERS = ",
    JSON.stringify(entries),
    ";",
    ""
  ].join("\n");

  fs.writeFileSync(OUT_PATH, body, "utf8");
  console.log(`wrote ${entries.length} entries to ${path.relative(ROOT, OUT_PATH)}`);
}

function parseCsv(text) {
  const lines = text.replace(/^\uFEFF/, "").split(/\r?\n/).filter(Boolean);
  const headers = splitCsvLine(lines.shift());
  return lines.map((line) => {
    const cells = splitCsvLine(line);
    return Object.fromEntries(headers.map((header, index) => [header, cells[index] || ""]));
  });
}

function splitCsvLine(line) {
  const cells = [];
  let cell = "";
  let quoted = false;
  for (let i = 0; i < line.length; i += 1) {
    const ch = line[i];
    if (ch === "\"" && line[i + 1] === "\"") {
      cell += "\"";
      i += 1;
    } else if (ch === "\"") {
      quoted = !quoted;
    } else if (ch === "," && !quoted) {
      cells.push(cell);
      cell = "";
    } else {
      cell += ch;
    }
  }
  cells.push(cell);
  return cells;
}

function indexAssetFiles(root) {
  const map = new Map();
  walk(root).forEach((file) => {
    if (/\.(png|jpg|jpeg|webp)$/i.test(file)) {
      map.set(path.basename(file).toLowerCase(), file);
    }
  });
  return map;
}

function walk(dir, out = []) {
  fs.readdirSync(dir, { withFileTypes: true }).forEach((entry) => {
    const file = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(file, out);
    else out.push(file);
  });
  return out;
}

function toWebPath(value) {
  return value.split(path.sep).map(encodeURIComponent).join("/");
}

function decodeImage(file) {
  const size = getImageSize(file);
  const raw = execFileSync("ffmpeg", ["-v", "error", "-i", file, "-f", "rawvideo", "-pix_fmt", "rgba", "-"], {
    maxBuffer: Math.max(16 * 1024 * 1024, size.width * size.height * 4 + 1024)
  });
  return { width: size.width, height: size.height, data: new Uint8Array(raw) };
}

function getImageSize(file) {
  const b = fs.readFileSync(file);
  if (b.length >= 24 && b.toString("ascii", 0, 4) === "RIFF" && b.toString("ascii", 8, 12) === "WEBP") {
    let off = 12;
    while (off + 8 <= b.length) {
      const type = b.toString("ascii", off, off + 4);
      const len = b.readUInt32LE(off + 4);
      const data = off + 8;
      if (type === "VP8X") {
        return { width: 1 + b.readUIntLE(data + 4, 3), height: 1 + b.readUIntLE(data + 7, 3) };
      }
      if (type === "VP8 ") {
        return { width: b.readUInt16LE(data + 6) & 0x3fff, height: b.readUInt16LE(data + 8) & 0x3fff };
      }
      if (type === "VP8L") {
        return {
          width: 1 + (((b[data + 2] & 0x3f) << 8) | b[data + 1]),
          height: 1 + (((b[data + 4] & 0x0f) << 10) | (b[data + 3] << 2) | ((b[data + 2] & 0xc0) >> 6))
        };
      }
      off = data + len + (len % 2);
    }
  }
  if (b.length >= 24 && b.readUInt32BE(12) === 0x49484452) {
    return { width: b.readUInt32BE(16), height: b.readUInt32BE(20) };
  }
  const probe = execFileSync("ffprobe", ["-v", "error", "-select_streams", "v:0", "-show_entries", "stream=width,height", "-of", "csv=p=0:s=x", file], {
    encoding: "utf8"
  }).trim();
  const [width, height] = probe.split("x").map(Number);
  return { width, height };
}

function describeReference(image) {
  const bounds = foregroundBounds(image);
  const variants = REFERENCE_ROI_VARIANTS.map((roi) => describeReferenceWithRoi(image, bounds, roi));
  return { ...variants[0], variants };
}

function describeReferenceWithRoi(image, bounds, roi) {
  const portraitRect = rectFromRoi(bounds, roi.portrait);
  const upperRect = rectFromRoi(bounds, roi.upper);
  const faceRect = rectFromRoi(bounds, roi.face);
  return describeRects(image.data, image.width, image.height, roi.name, portraitRect, upperRect, faceRect);
}

function describeRects(data, width, height, name, portraitRect, upperRect, faceRect) {
  return {
    name,
    version: "feature-map-v1",
    parts: {
      portrait: featureMap(data, width, height, portraitRect, FEATURE_SIZE),
      upper: featureMap(data, width, height, upperRect, FEATURE_SIZE),
      face: featureMap(data, width, height, faceRect, FEATURE_SIZE)
    }
  };
}

function foregroundBounds(image) {
  let minX = image.width;
  let minY = image.height;
  let maxX = 0;
  let maxY = 0;
  let found = false;
  for (let y = 0; y < image.height; y += 1) {
    for (let x = 0; x < image.width; x += 1) {
      const a = image.data[(y * image.width + x) * 4 + 3];
      if (a > 24) {
        minX = Math.min(minX, x);
        minY = Math.min(minY, y);
        maxX = Math.max(maxX, x);
        maxY = Math.max(maxY, y);
        found = true;
      }
    }
  }
  if (!found) return { x: 0, y: 0, w: image.width, h: image.height };
  return { x: minX, y: minY, w: maxX - minX + 1, h: maxY - minY + 1 };
}

function rectFromRoi(rect, roi) {
  return {
    x: rect.x + rect.w * roi.x,
    y: rect.y + rect.h * roi.y,
    w: rect.w * roi.w,
    h: rect.h * roi.h
  };
}

function featureMap(data, width, height, rect, size) {
  const l = [];
  const rg = [];
  const yb = [];
  const a = [];
  for (let y = 0; y < size; y += 1) {
    for (let x = 0; x < size; x += 1) {
      const sx = clamp(Math.round(rect.x + ((x + 0.5) / size) * rect.w), 0, width - 1);
      const sy = clamp(Math.round(rect.y + ((y + 0.5) / size) * rect.h), 0, height - 1);
      const idx = (sy * width + sx) * 4;
      const alpha = data[idx + 3] / 255;
      const r = data[idx] * alpha + 255 * (1 - alpha);
      const g = data[idx + 1] * alpha + 255 * (1 - alpha);
      const b = data[idx + 2] * alpha + 255 * (1 - alpha);
      l.push(Math.round(r * 0.299 + g * 0.587 + b * 0.114));
      rg.push(Math.round(r - g));
      yb.push(Math.round((r + g) * 0.5 - b));
      a.push(Number(alpha.toFixed(3)));
    }
  }
  return { size, l, rg, yb, e: edgeMap(l, size), a };
}

function edgeMap(luma, size) {
  const out = [];
  for (let y = 0; y < size; y += 1) {
    for (let x = 0; x < size; x += 1) {
      const here = luma[y * size + x];
      const right = x < size - 1 ? luma[y * size + x + 1] : here;
      const down = y < size - 1 ? luma[(y + 1) * size + x] : here;
      out.push(Math.round(Math.hypot(here - right, here - down)));
    }
  }
  return out;
}

function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

main();
